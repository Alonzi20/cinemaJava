CREATE TABLE Film (
                         id BIGINT AUTO_INCREMENT PRIMARY KEY,
                         title VARCHAR(100) NOT NULL,
                         overview VARCHAR(500),
                         release_date VARCHAR(10),
                         poster_path VARCHAR(100),
                         genres VARCHAR(100),
                         duration INT,
                         adult BOOLEAN
